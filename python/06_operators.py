# Logical operators
bool1 = True
bool2 = False
print("The value of bool1 and bool2 is", (bool1 and bool2))
print("The value of bool1 or bool2 is", (bool1 or bool2))
print("The value of not bool1 and bool2 is", (not bool2))
# In and, it will print True if both are True
# In or, it will print True if either of them is True
# But in not, it exchange the True as False and vice versa